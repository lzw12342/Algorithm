function T = DErand1(P, F, n)
% DE/rand/1 变异算子（向量化 + 小群体安全版）
% 输入：
%   P : (D × N) 父代种群
%   F : 缩放因子（标量或 1×n）
%   n : 需要生成的子代数量（默认 = N）
% 输出：
%   T : (D × n) 子代

    N = size(P,2);
    if nargin < 3
        n = N;
    end

    % 为 n 个子代各自从 P 中随机选 r1,r2,r3（允许重复选同一个体的不同位置）
    % 思路：生成 n 列、每列从 1:N 中选3个不重复索引
    % 用随机排列矩阵：(N × n)，取前3行
    [~, rnd] = sort(rand(N, n), 1);    % (N × n)，每列是 1:N 的随机排列

    r1 = rnd(1,:);
    r2 = rnd(2,:);
    r3 = rnd(3,:);

    T = P(:,r1) + F .* (P(:,r2) - P(:,r3));  % (D × n)
end
